// scripts/setup-pdf-worker.js
//
// Copies the pdf.js worker into public/ and wraps it with a polyfill
// shim, since editing pdf.worker.min.mjs directly gets wiped out by
// npm install every time. This regenerates both files on every install.

const fs = require('fs')
const path = require('path')

const src = path.join(__dirname, '..', 'node_modules', 'pdfjs-dist', 'build', 'pdf.worker.min.mjs')
const realDest = path.join(__dirname, '..', 'public', 'pdf.worker.real.min.mjs')
const wrapperDest = path.join(__dirname, '..', 'public', 'pdf.worker.min.mjs')

fs.copyFileSync(src, realDest)

const wrapper = `// Auto-generated by scripts/setup-pdf-worker.js — do not edit directly.
// Polyfills newer TC39 methods inside the WORKER's own global scope
// before loading the real pdf.js worker, since this is a separate JS
// realm from the main thread and does not share its prototypes.

if (!Uint8Array.prototype.toHex) {
  Uint8Array.prototype.toHex = function () {
    return Array.from(this, (b) => b.toString(16).padStart(2, '0')).join('')
  }
}
if (!Uint8Array.fromHex) {
  Uint8Array.fromHex = function (hex) {
    const bytes = new Uint8Array(hex.length / 2)
    for (let i = 0; i < bytes.length; i++) {
      bytes[i] = parseInt(hex.substr(i * 2, 2), 16)
    }
    return bytes
  }
}
if (!Uint8Array.prototype.toBase64) {
  Uint8Array.prototype.toBase64 = function () {
    let binary = ''
    for (let i = 0; i < this.length; i++) binary += String.fromCharCode(this[i])
    return btoa(binary)
  }
}
if (!Uint8Array.fromBase64) {
  Uint8Array.fromBase64 = function (base64) {
    const binary = atob(base64)
    const bytes = new Uint8Array(binary.length)
    for (let i = 0; i < binary.length; i++) bytes[i] = binary.charCodeAt(i)
    return bytes
  }
}
if (!Map.prototype.getOrInsertComputed) {
  Map.prototype.getOrInsertComputed = function (key, callbackFn) {
    if (this.has(key)) return this.get(key)
    const value = callbackFn(key)
    this.set(key, value)
    return value
  }
}
if (!WeakMap.prototype.getOrInsertComputed) {
  WeakMap.prototype.getOrInsertComputed = function (key, callbackFn) {
    if (this.has(key)) return this.get(key)
    const value = callbackFn(key)
    this.set(key, value)
    return value
  }
}

import('./pdf.worker.real.min.mjs')
`

fs.writeFileSync(wrapperDest, wrapper)
console.log('Generated public/pdf.worker.min.mjs (polyfill wrapper) and public/pdf.worker.real.min.mjs')